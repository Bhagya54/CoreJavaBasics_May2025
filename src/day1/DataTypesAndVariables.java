package day1;

public class DataTypesAndVariables {
	
	/*variable - container to store the data
	 * type of data - data type
	 * 1.Primitive:
	 * 		Numeric: 23,67--- byte,short,int,long
	 * 		Decimal: 23.56,89.45 -- float,double
	 * 		character : char:a,b,#,:
	 * 	true/false: boolean
	 * 
	 * 2.Non Primitive: class based
	 * String,class,Array,Collection
	 * 
	 * Mediator: Wrapper class
	 * byte : Byte
	 * short: Short
	 * int: Integer
	 * long: Long
	 * float: Float
	 * double: Double
	 * char: Character
	 * boolean: Boolean
	 */

	public static void main(String[] args) {
		byte a=-128;
		float b=34.45534f;
		char c = 'a';
		boolean d=true;
		
		String str="hello";
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(str);
		

	}

}
